/* for eagle */

const eagleSpeaker = document.getElementById('eagle-speaker');
const eagleSound = document.getElementById('eagle-sound');
const eagle = document.getElementById('eagle');
const eagleTranslator = document.getElementById('eagle-translator');
const eagleGeorgianName = document.querySelector('.eagle-georgian-description');
const eagleEnglishName = document.querySelector('.eagle-english-description');

eagleSpeaker.addEventListener('click', () => {
    if (window.getComputedStyle(eagleGeorgianName).display == 'block') {
        eagleSound.play();
    } else {
        eagle.play();
    }
});

eagleTranslator.addEventListener('click', () => {
    if (window.getComputedStyle(eagleGeorgianName).display == 'block') {
        eagleGeorgianName.style.display = 'none';
        eagleEnglishName.style.display = 'block';
    } else {
        eagleEnglishName.style.display = 'none';
        eagleGeorgianName.style.display = 'block';
    }
});

/* for eagle */


