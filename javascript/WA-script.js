/* for lion */

const lionSpeaker = document.getElementById('lion-speaker');
const lionSound = document.getElementById('lion-sound');
const lion = document.getElementById('lion');
const lionTranslator = document.getElementById('lion-translator');
const lionGeorgianName = document.querySelector('.lion-georgian-description');
const lionEnglishName = document.querySelector('.lion-english-description');

lionSpeaker.addEventListener('click', () => {
    if (window.getComputedStyle(lionGeorgianName).display == 'block') {
        lionSound.play();
    } else {
        lion.play();
    }
});

lionTranslator.addEventListener('click', () => {
    if (window.getComputedStyle(lionGeorgianName).display == 'block') {
        lionGeorgianName.style.display = 'none';
        lionEnglishName.style.display = 'block';
    } else {
        lionEnglishName.style.display = 'none';
        lionGeorgianName.style.display = 'block';
    }
});

/* for lion */


